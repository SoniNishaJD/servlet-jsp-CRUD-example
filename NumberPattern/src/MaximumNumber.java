
public class MaximumNumber {

	public static void main(String[] args) {
		int a = 50, b = 20, c = 30;

		if (a >= b && a >= c) {
			System.out.println("a is maximum number");
		} else if (b >= a && b >= c) {
			System.out.println("b is maximum number");
		} else {
			System.out.println("c is maximum number");
		}

	}
}
